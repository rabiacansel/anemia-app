def build_dataframe(image_paths, modality):
    rows = []

    for path in image_paths:
        patient_key, label, label_text = extract_patient_key(path)

        if patient_key is None:
            continue

        rows.append({
            "path": path,
            "patient_key": patient_key,
            "label": label,
            "class": label_text,
            "modality": modality
        })

    return pd.DataFrame(rows)

