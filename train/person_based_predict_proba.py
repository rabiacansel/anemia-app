def make_prob_df(test_df, probs, name):
    temp = test_df.copy()
    temp[name] = probs

    patient_prob = temp.groupby("patient_key").agg(
        y_true=("label", "first"),
        prob=(name, "mean")
    ).reset_index()

    patient_prob = patient_prob.rename(columns={"prob": name})

    return patient_prob

