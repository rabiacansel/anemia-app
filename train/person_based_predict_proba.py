import cv2
import numpy as np

def extract_features_from_df(df_exp):
    groups = []
    features = []
    labels = []

    for _, row in df_exp.iterrows():
        img = cv2.imread(row["path"])
        if img is None:
            continue

        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        R = img[:, :, 0]
        G = img[:, :, 1]
        B = img[:, :, 2]

        gray = cv2.cvtColor(img, cv2.COLOR_RGB2GRAY)
        hsv = cv2.cvtColor(img, cv2.COLOR_RGB2HSV)
        H, S, V = cv2.split(hsv)

        lab = cv2.cvtColor(img, cv2.COLOR_RGB2LAB)
        L, a, b = cv2.split(lab)

        R_mean = np.mean(R)
        G_mean = np.mean(G)
        B_mean = np.mean(B)

        features.append([
            np.mean(R), np.std(R),
            np.mean(G), np.std(G),
            np.mean(B), np.std(B),

            np.mean(L), np.std(L),
            np.mean(a), np.std(a),
            np.mean(b), np.std(b),

            np.mean(H), np.std(H),
            np.mean(S), np.std(S),
            np.mean(V), np.std(V),

            np.percentile(R, 25), np.percentile(R, 50), np.percentile(R, 75),
            np.percentile(G, 25), np.percentile(G, 50), np.percentile(G, 75),
            np.percentile(B, 25), np.percentile(B, 50), np.percentile(B, 75),

            R_mean / (G_mean + 1e-6),
            R_mean / (B_mean + 1e-6),
            G_mean / (B_mean + 1e-6),

            R_mean / (R_mean + G_mean + B_mean + 1e-6),
            G_mean / (R_mean + G_mean + B_mean + 1e-6),
            B_mean / (R_mean + G_mean + B_mean + 1e-6),

            np.mean(gray),
            np.std(gray),
            np.var(gray)
        ])

        labels.append(1 if row["class"] == "Anemic" else 0)
        groups.append(row["patient_key"])

    X = np.array(features, dtype=np.float32)
    y = np.array(labels, dtype=np.int32)
    groups = np.array(groups)

    return X, y, groups

